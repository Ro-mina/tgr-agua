    nombre_upper = nombre.upper().strip()
    # Intento exacto primero
    if nombre_upper in COMUNAS_SII:
        return COMUNAS_SII[nombre_upper]
    # Intento parcial (por si viene con acento o variación menor)
    for key, codigo in COMUNAS_SII.items():
        if key in nombre_upper or nombre_upper in key:
            return codigo
    return 0